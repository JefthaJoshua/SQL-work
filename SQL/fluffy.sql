CREATE TABLE TBL_CUSTOMER (
    CUSTOMER_ID  INT,
    FIRST_NAME VARCHAR(50) NOT NULL,
    SURNAME  VARCHAR(50) NOT NULL,
    ADDRESS VARCHAR (255) NOT NULL,
    CONTACT_NUMBER VARCHAR (50) NOT NULL,
    EMAIL VARCHAR(255) NOT NULL,
    CONSTRAINT pk_CUSTOMER PRIMARY KEY(CUSTOMER_ID)
);
CREATE TABLE TBL_EMPLOYEE(
    EMPLOYEE_ID  VARCHAR (15),
    FIRST_NAME VARCHAR(50) NOT NULL,
    SURNAME   VARCHAR(50) NOT NULL,
    CONTACT_NUMBER VARCHAR (50) NOT NULL,
    "POSITION" VARCHAR (50) NOT NULL,
    ADDRESS VARCHAR (255) NOT NULL,
    EMAIL VARCHAR (255) NOT NULL,
    CONSTRAINT pk_EMPLOYEE PRIMARY KEY(EMPLOYEE_ID)
);
CREATE TABLE TBL_DELIVERY (
    DELIVERY_ID  INT,
    "DESCRIPTION" VARCHAR(255) NOT NULL,
    DISPATCH_DATE  DATE NOT NULL,
    DELIVERY_DATE DATE NOT NULL,
    CONSTRAINT pk_RETURNS PRIMARY KEY(DELIVERY_ID)
);
CREATE TABLE TBL_RETURNS (
    RETURN_ID  VARCHAR (15),
    RETURN_DATE  DATE NOT NULL,
    REASON VARCHAR(255) NOT NULL,
    CONSTRAINT TBL_ PRIMARY KEY(RETURN_ID)
);
CREATE TABLE TBL_PRODUCT(
    PRODUCT_ID INT,
    PRODUCT  VARCHAR(255) NOT NULL,
    PRICE  NUMBER(9,2) NOT NULL,
    QTY INT NOT NULL,
    CONSTRAINT pk_PRODUCT PRIMARY KEY(PRODUCT_ID)
);
CREATE TABLE TBL_BILLING(
    BILL_ID  INT,
    CUSTOMER_ID  INT NOT NULL,
    BILL_DATE   DATE NOT NULL,
    EMPLOYEE_ID VARCHAR(15) NOT NULL,
    CONSTRAINT pk_BILLING PRIMARY KEY(BILL_ID),
    CONSTRAINT fk_TBL_BILLING1 FOREIGN KEY (CUSTOMER_ID) REFERENCES TBL_CUSTOMER(CUSTOMER_ID),
    CONSTRAINT fk_TBL_BILLING2 FOREIGN KEY (EMPLOYEE_ID) REFERENCES TBL_EMPLOYEE(EMPLOYEE_ID)
);
CREATE TABLE TBL_PRODUCT_BILLING(
    DELIVERY_ID  INT,
    RETURN_ID VARCHAR(15),
    PRODUCT_ID INT,
    BILL_ID INT,
    CONSTRAINT PK_BILL PRIMARY KEY (BILL_ID,PRODUCT_ID,DELIVERY_ID),
    CONSTRAINT fk_PRODUCT_BILLING1 FOREIGN KEY (DELIVERY_ID) REFERENCES departments(DELIVERY_ID),
    CONSTRAINT fk_PRODUCT_BILLING2 FOREIGN KEY (RETURN_ID) REFERENCES departments(RETURN_ID),
    CONSTRAINT fk_PRODUCT_BILLING3 FOREIGN KEY (PRODUCT_ID) REFERENCES departments(PRODUCT_ID ),
    CONSTRAINT fk_PRODUCT_BILLING4 FOREIGN KEY (BILL_ID) REFERENCES departments(BILL_ID)
);
